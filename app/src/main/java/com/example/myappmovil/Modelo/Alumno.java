package com.example.myappmovil.Modelo;

public class Alumno {
    public String nombre;
    public String apellido;
    public int edad;

    public Alumno() {
    }

    public Alumno(String nombre, String apellido, int edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
    }
}
